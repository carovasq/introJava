package org.example;

public class PrimitiveTypes {
    public static void main(String[] args){

        //escribir un boolean, asignarlo, mostrar por consola
        boolean isActive = true;

        System.out.println("Boolean: " + isActive);


        //Tipos primitivos: enteros
        byte isByte = 1;
        short isShort = 2;
        int isInt = 3;
        long isLong = 4;

        System.out.println("Byte: " + isByte);
        System.out.println("Short: " + isShort);
        System.out.println("Int: " + isInt);
        System.out.println("Long: " + isLong);


        //Tipos primitivos: punto flotante
        float isFloat = 5.1f;
        double isDouble = 1.5d;

        System.out.println("Float: " + isFloat);
        System.out.println("Double: " + isDouble);


        //Tipos primitivos: punto flotante
        char isChar = 'A';

        System.out.println("Char: " + isChar);


    }
}